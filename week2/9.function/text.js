/* 함수(function)
- 자주 쓰는 실행코드들을 블럭({})단위로 묶어서 패키징 해놓은 상태
- 그런데 자바스크립트에서는 파라미터로 함수를 넘길 수 있다.
- 자주 쓰는 코드들을 기능단위로 재사용하기 위함
- 함수 선언: function예약어를 이용한다.
- 함수 호출: 정의된 함수를 호출해야 기능이 실행된다.
*/

/* 함수 종류
1. 선언적 함수: 이름을 가진다.
- 자바스크립트 파일을 읽을 때 선언적 함수를 가장 먼저 읽어준다.
- 선언적 함수 호출 위치가 자유롭다.

2. 익명 함수: 함수의 이름이 없는 형태이다. - callback
- 자체만으로 호출이 불가능하다.
- 변수에 익명함수를 대입 또는 특정 이벤트 객체에 대입하는 형태로 호출한다.

3. 대입함수: 변수에 익명함수가 대입된 형태

4. 즉시실행함수: KAKAO API - 지도 서비스 코드 형태*/
